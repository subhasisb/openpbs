/**
 * @brief
 *	Prepare all the node related sqls. Typically called after connect
 *	and before any other sql exeuction
 *
 * @param[in]	conn - Database connection handle
 *
 * @return      Error code
 * @retval	-1 - Failure
 * @retval	 0 - Success
 *
 */
int
pg_db_prepare_nodejob_sqls(pbs_db_conn_t *conn)
{
	snprintf(conn->conn_sql, MAX_SQL_LENGTH, "update pbs.node_job set "
			"nj_savetm = localtimestamp,"
			"attributes = attributes || hstore($3::text[]) "
			"where job_id = $1 and nd_name = $2");

	if (pg_prepare_stmt(conn, STMT_UPDATE_NODEJOBATTRS, conn->conn_sql, 3) != 0)
		return -1;

	snprintf(conn->conn_sql, MAX_SQL_LENGTH, "select "
		"job_id, "
		"nd_name, "
		"is_resv, "
		"admn_suspend, "
		"hstore_to_array(attributes) as attributes "
		"from pbs.node_job "
		"where job_id = $1 and nd_name = $2 "
		"order by nj_creattm");
	if (pg_prepare_stmt(conn, STMT_SELECT_NODEJOB, conn->conn_sql, 2) != 0)
		return -1;

	snprintf(conn->conn_sql, MAX_SQL_LENGTH, "select "
		"job_id, "
		"nd_name, "
		"is_resv, "
		"admn_suspend, "
		"hstore_to_array(attributes) as attributes "
		"from pbs.node_job "
		"where nd_name = $1 "
		"order by nj_creattm");
	if (pg_prepare_stmt(conn, STMT_FIND_NODEJOB_USING_NODEID, conn->conn_sql, 1) != 0)
		return -1;

	snprintf(conn->conn_sql, MAX_SQL_LENGTH, "insert into pbs.node_job("
		"job_id, "
		"nd_name, "
		"is_resv, "
		"admn_suspend, "
		"nj_creattm, "
		"nj_savetm, "
		"attributes "
		") "
		"values "
		"($1, $2, $3, $4, localtimestamp, localtimestamp, hstore($5::text[]))");

	if (pg_prepare_stmt(conn, STMT_INSERT_NODEJOB, conn->conn_sql, 5) != 0)
		return -1;

	/* in case of nodes do not use || with existing attributes, since we re-write all attributes */
	snprintf(conn->conn_sql, MAX_SQL_LENGTH, "update pbs.node_job set "
		"is_resv = $3, "
		"admn_suspend = $4, "
		"nj_savetm = localtimestamp, "
		"attributes = hstore($5::text[]) "
		" where job_id = $1 and nd_name = $2");
	if (pg_prepare_stmt(conn, STMT_UPDATE_NODEJOB, conn->conn_sql, 5) != 0)
		return -1;

	snprintf(conn->conn_sql, MAX_SQL_LENGTH, "delete from pbs.node_job where job_id = $1");
	if (pg_prepare_stmt(conn, STMT_DELETE_NODEJOB, conn->conn_sql, 1) != 0)
		return -1;

	return 0;
}



/**
 * @brief
 *	Insert nodejob data into the database
 *
 * @param[in]	conn - Connection handle
 * @param[in]	obj  - Information of nodejob to be inserted
 *
 * @return      Error code
 * @retval	-1 - Failure
 * @retval	 0 - Success
 *
 */
int
pg_db_save_nodejob(pbs_db_conn_t *conn, pbs_db_obj_info_t *obj, int savetype)
{
	pbs_db_nodejob_info_t *pndjob = obj->pbs_db_un.pbs_db_nodejob;
	char *stmt;
	int params;
	char *raw_array = NULL;

	SET_PARAM_STR(conn, pndjob->job_id, 0);
	SET_PARAM_STR(conn, pndjob->nd_name, 1);
	SET_PARAM_INTEGER(conn, pndjob->is_resv, 2);
	SET_PARAM_INTEGER(conn, pndjob->admn_suspend, 3);

	if (savetype == PBS_UPDATE_DB_QUICK) {
		params = 4;
	} else {
		int len = 0;
		/* convert attributes to postgres raw array format */
		if ((len = convert_db_attr_list_to_array(&raw_array, &pndjob->attr_list)) <= 0)
			return -1;

		SET_PARAM_BIN(conn, raw_array, len, 4);
		params = 5;
	}

	if (savetype == PBS_UPDATE_DB_FULL)
		stmt = STMT_UPDATE_NODEJOB;
	else
		stmt = STMT_INSERT_NODEJOB;

	if (pg_db_cmd(conn, stmt, params) != 0) {
		free(raw_array);
		return -1;
	}

	free(raw_array);

	return 0;
}

/**
 * @brief
 *	Add or update attributes of a node_job table
 *
 * @param[in]	conn - Connection handle
 * @param[in]	obj  - Node information
 * @param[in]	node_id  - Node name
 * @param[in]	attr_list - List of attributes
 *
 * @return      Error code
 * @retval	-1 - Execution of prepared statement failed
 * @retval	 0 - Success and > 0 rows were affected
 * @retval	 1 - Execution succeeded but statement did not affect any rows
 *
 */
int
pg_db_add_update_attr_nodejob(pbs_db_conn_t *conn, pbs_db_obj_info_t *obj, void *node_id, pbs_db_attr_list_t *attr_list)
{
	char *raw_array = NULL;
	int len = 0;
	pbs_db_nodejob_info_t *pnj = obj->pbs_db_un.pbs_db_nodejob;

	if ((len = convert_db_attr_list_to_array(&raw_array, attr_list)) <= 0)
		return -1;
	SET_PARAM_STR(conn, pnj->job_id, 0);
	SET_PARAM_STR(conn, node_id, 1);

	SET_PARAM_BIN(conn, raw_array, len, 2);

	if (pg_db_cmd(conn, STMT_UPDATE_NODEJOBATTRS, 3) != 0) {
		free(raw_array);
		return -1;
	}

	free(raw_array);

	return 0;
}

/**
 * @brief
 *	Load node-job data from the database
 *
 * @param[in]	conn - Connection handle
 * @param[in]	obj  - Load node information into this object
 *
 * @return      Error code
 * @retval	-1 - Failure
 * @retval	 0 - Success
 * @retval	>1 - Number of attributes
 * @retval 	-2 -  Success but data same as old, so not loading data (but locking if lock requested)
 *
 */
int
pg_db_load_nodejob(pbs_db_conn_t *conn, pbs_db_obj_info_t *obj, int lock)
{
	PGresult *res;
	int rc;
	pbs_db_nodejob_info_t *pnj = obj->pbs_db_un.pbs_db_nodejob;

	SET_PARAM_STR(conn, pnj->job_id, 0);
	SET_PARAM_STR(conn, pnj->nd_name, 1);
	if ((rc = pg_db_query(conn, STMT_SELECT_NODEJOB, 2, lock, &res)) != 0)
		return rc;

	rc = load_nodejob(res, pnj, 0);

	PQclear(res);
	return rc;
}

/**
 * @brief
 *	Find nodejob
 *
 * @param[in]	conn - Connection handle
 * @param[in]	st   - The cursor state variable updated by this query
 * @param[in]	obj  - Information of nodejob to be found
 * @param[in]	opts - Any other options (like flags, timestamp)
 *
 * @return      Error code
 * @retval	-1 - Failure
 * @retval	 0 - Success
 * @retval	 1 - Success, but no rows found
 *
 */
int
pg_db_find_nodejob(pbs_db_conn_t *conn, void *st, pbs_db_obj_info_t *obj,
	pbs_db_query_options_t *opts)
{
	PGresult *res;
	int rc;
	pg_query_state_t *state = (pg_query_state_t *) st;
	pbs_db_nodejob_info_t *pnj = obj->pbs_db_un.pbs_db_nodejob;

	if (!state)
		return -1;

	SET_PARAM_STR(conn, pnj->nd_name, 0);
	if ((rc = pg_db_query(conn, STMT_FIND_NODEJOB_USING_NODEID, 1, 0, &res)) != 0)
		return rc;

	state->row = 0;
	state->res = res;
	state->count = PQntuples(res);
	return 0;
}

/**
 * @brief
 *	Get the next nodejob from the cursor
 *
 * @param[in]	conn - Connection handle
 * @param[in]	st   - The cursor state
 * @param[in]	obj  - Node information is loaded into this object
 *
 * @return      Error code
 *		(Even though this returns only 0 now, keeping it as int
 *			to support future change to return a failure)
 * @retval	 0 - Success
 *
 */
int
pg_db_next_nodejob(pbs_db_conn_t *conn, void *st, pbs_db_obj_info_t *obj)
{
	PGresult *res = ((pg_query_state_t *) st)->res;
	pg_query_state_t *state = (pg_query_state_t *) st;

	return (load_nodejob(res, obj->pbs_db_un.pbs_db_nodejob, state->row));
}

/**
 * @brief
 *	Frees allocate memory of an Object
 *
 * @param[in]	obj - pbs_db_obj_info_t containing the DB object
 *
 * @return None
 *
 */
void
pg_db_reset_nodejob(pbs_db_obj_info_t *obj)
{
	free_db_attr_list(&(obj->pbs_db_un.pbs_db_nodejob->attr_list));
}

/**
 * @brief
 *	Delete the nodejob from the database
 *
 * @param[in]	conn - Connection handle
 * @param[in]	obj  - Node information
 * @param[in]	opts - Optional arguments (i.e. timestamp or flags)
 *
 * @return      Error code
 * @retval	-1 - Failure
 * @retval	 0 - Success
 * @retval	 1 - Success but no rows deleted
 *
 */
int
pg_db_delete_nodejob(pbs_db_conn_t *conn, pbs_db_obj_info_t *obj, pbs_db_query_options_t *opts)
{
	pbs_db_nodejob_info_t *pnd = obj->pbs_db_un.pbs_db_nodejob;
	SET_PARAM_STR(conn, pnd->job_id, 0);
	return (pg_db_cmd(conn, STMT_DELETE_NODEJOB, 1));
}
